// Package specdrafting 实现专利说明书（发明/实用新型）的独立撰写模块。
//
// 本模块接收 disclosure 管线的结构化 ExtractionResult 作为输入，
// 依据专利法第 26 条第 3 款（充分公开）和专利法实施细则第 18 条，
// 生成符合法律要求的完整说明书（技术领域、背景技术、发明内容、
// 附图说明、具体实施方式、名称、摘要）。
//
// 双专利类型支持：
//   - 发明专利（PatentTypeInvention）：保护产品/方法，可不带附图
//   - 实用新型（PatentTypeUtilityModel）：仅保护产品（形状/结构），必须有附图
//
// 四大技术领域自适应：
//   - 机械（mechanical）：零部件+配置关系+联系形式
//   - 电学/电路（electrical）：元器件+导线连接+电回路+功能
//   - 化学/材料（chemical）：组分及含量，效果依赖实验数据
//   - 软件/计算机（software）：方法步骤或功能模块架构
//
// 架构模式：
//
//	SpecInput → Pregel 图引擎（12 节点线性执行）→ SpecOutput
//	                                                   ↓
//	                                        RuleEngine 验证 → SpecScorer 评分
//
// 法律依据：
//   - 专利法第 26 条第 3 款：说明书应当对发明或者实用新型作出清楚、完整的说明，
//     以所属技术领域的技术人员能够实现为准
//   - 专利法实施细则第 18 条：说明书的撰写顺序和方式
//   - 审查指南第二部分第二章第 2.1 节：说明书应当满足清楚、完整、能够实现的要求
//   - 审查指南第一部分第二章第 7 节：实用新型初审的特殊要求
