// Package inventiveness 提供专利法第22条第3款（创造性/非显而易见性）判断的独立 Pregel 子图。
//
// 本模块对标 domains/enablement/（充分公开评估），通过 EventBus 接收
// disclosure 管线完成事件，自动评估技术方案是否具备创造性。
//
// # 法条依据
//
//	《中华人民共和国专利法》（2020 年修正）第 22 条第 3 款（简称 A22.3 / 22.3）：
//	"创造性，是指与现有技术相比，该发明具有突出的实质性特点和显著的进步，
//	 该实用新型具有实质性特点和进步。"
//
// # 判断方法（三步法）
//
// 根据中国专利审查指南第二部分第四章，创造性判断采用三步法：
//
//	Step 1: 确定最接近的现有技术
//	  — 技术领域相同或相近、公开特征最多、技术问题最接近
//	Step 2: 确定区别特征和发明实际解决的技术问题
//	  — 区别特征必须是权利要求明确记载的特征
//	  — 实际技术问题可重新确定（不同于申请文件声称的问题）
//	Step 3: 判断现有技术整体上是否存在技术启示
//	  — 公知常识 → 显而易见
//	  — 其他对比文件公开区别特征且作用相同 → 有结合启示
//	  — 禁止"事后诸葛亮"式分析
//	  — 预料不到的技术效果可推翻形式上的技术启示
//
// # 使用方式
//
//  1. EventBus 自动触发：disclosure 管线完成后通过 InventivenessTrigger 自动运行
//  2. Agent 工具调用：通过 NewInventivenessTool() 注册为 Patent Agent 工具
//
// # 辅助判断因素
//
//	技术启示（TSM test） / 公知常识 / 预料不到的技术效果 /
//	克服技术偏见 / 长期需求未满足 / 商业成功
//
// # 关键词
//
//	专利法第22条第3款、22.3、A22.3、创造性、三步法、inventive step、
//	非显而易见性、技术启示、TSM test、obviousness、发明高度
