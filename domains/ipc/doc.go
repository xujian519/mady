// Package ipc 提供 IPC 国际专利分类（International Patent Classification）技术领域
// 的分类、识别和领域特化分析能力。
//
// # 用途
//
// IPC 分为 8 个大类（A-H），不同技术领域的专利审查标准存在显著差异。
// 本包为分析工作流提供以下支持：
//
//  1. IPC 分类识别（Classify / ClassifyDetailed）——从专利文本中自动判定所属 IPC 大类
//  2. 领域特化提示词（GetInventivenessHints / GetNoveltyHints）——按 IPC 大类返回
//     有针对性的审查要点提示，供 LLM 分析节点使用
//  3. 公知常识示例（GetCommonKnowledge）——该领域典型公知常识列表
//
// # 设计原则
//
// - 关键词规则优先：无需 LLM 调用即可完成初步分类，低延迟、零成本
// - 领域中立：IPC 信息作为分析增强而非必需条件，不影响现有工作流
// - 扩展友好：新增 IPC 子类只需在 AllDomains 中添加条目
//
// # IPC 八大类
//
//	A: 人类生活必需（农、医、食品、轻工）
//	B: 作业/运输（分离、混合、成型、印刷、运输）
//	C: 化学/冶金（化学、冶金、玻璃、燃料）
//	D: 纺织/造纸（纺织、纤维、造纸）
//	E: 固定建筑物（建筑、道路、水利、采矿）
//	F: 机械工程（发动机、泵、一般机械）
//	G: 物理（测量、光学、计算、控制）
//	H: 电学（电路、通信、发电）
