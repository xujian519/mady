// Package novelty 提供专利法第22条第2款（新颖性/Novelty）判断的独立 Pregel 子图。
//
// 本模块对标 domains/inventiveness/（创造性）和 domains/enablement/（充分公开），
// 通过 EventBus 接收 disclosure 管线完成事件，自动评估技术方案是否具备新颖性。
//
// # 法条依据
//
//	《中华人民共和国专利法》（2020 年修正）第 22 条第 2 款（简称 A22.2 / 22.2）：
//	"新颖性，是指该发明或者实用新型不属于现有技术；也没有任何单位或者个人
//	 就同样的发明或者实用新型在申请日以前向国务院专利行政部门提出过申请，
//	 并记载在申请日以后公布的专利申请文件或者公告的专利文件中。"
//
//	《专利法》第 22 条第 5 款：现有技术的定义
//	《专利法》第 24 条：不丧失新颖性的宽限期
//	《专利法》第 29 条：优先权
//
// # 判断框架
//
// 根据中国专利审查指南第二部分第三章，新颖性判断包含以下维度：
//
//	Step 1: 现有技术审查（Prior Art Check）
//	  — 确定申请日/优先权日
//	  — 判断技术是否"为公众所知"
//	  — 判断在先文献是否充分公开（可实施性）
//
//	Step 2: 单独对比（Single Comparison）
//	  — 权利要求特征提取
//	  — 上下位概念判断
//	  — 惯用手段直接置换
//	  — 数值范围 8 种情形
//	  — 四要素综合判断（领域/问题/方案/效果）
//
//	Step 3: 抵触申请审查（Conflict Application Check）
//	  — 三要件检验（时间/公开/内容）
//	  — 全文内容制比对
//
//	Step 4: 宽限期与优先权例外（Grace Period & Priority）
//	  — 宽限期三种情形
//	  — 优先权"相同主题"判断
//
//	Step 5: 特殊领域规则（Special Domain Rules）
//	  — 化学领域特殊性
//	  — 参数/用途/制备方法特征
//
// # 使用方式
//
//  1. EventBus 自动触发：disclosure 管线完成后通过 NoveltyTrigger 自动运行
//  2. Agent 工具调用：通过 NewNoveltyTool() 注册为 Patent Agent 工具
//
// # 关键词
//
//	专利法第22条第2款、22.2、A22.2、新颖性、单独对比、现有技术、
//	抵触申请、宽限期、优先权、novelty、prior art、novelty search
