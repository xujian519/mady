// citationTopics 返回 statute 对应的主题表与存在性上限（0 = 不做存在性核验）。
func citationTopics(s lawcite.Statute) (topics map[int][]string, maxArticle int) {
	switch s {
	case lawcite.StatutePatentLaw:
		return patentLawTopics, maxPatentLawArticle
	case lawcite.StatuteImplementingRules:
		return implementingRulesTopics, 0
	case lawcite.StatuteTrademarkLaw:
		return trademarkLawTopics, maxTrademarkLawArticle
	case lawcite.StatuteCopyrightLaw:
		return copyrightLawTopics, maxCopyrightLawArticle
	case lawcite.StatuteAntiUnfairCompetition:
		return antiUnfairCompetitionTopics, maxAntiUnfairCompetitionArticle
	default:
		return nil, 0
	}
}
